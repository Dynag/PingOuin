    public void affichage(){
        tfHeure.setText(fdb.paramLire("PeriodeHeure", "options"));
        if(fdb.paramLire("PeriodeLun", "options").equals("1")){ cbLun.setSelected(true); }else{  cbLun.setSelected(false); }
        if(fdb.paramLire("PeriodeMar", "options").equals("1")){ cbMar.setSelected(true); }else{  cbLun.setSelected(false); }
        if(fdb.paramLire("PeriodeMerc", "options").equals("1")){ cbMerc.setSelected(true); }else{  cbLun.setSelected(false); }
        if(fdb.paramLire("PeriodeJeu", "options").equals("1")){ cbJeu.setSelected(true); }else{  cbLun.setSelected(false); }
        if(fdb.paramLire("PeriodeVen", "options").equals("1")){ cbVen.setSelected(true); }else{  cbLun.setSelected(false); }
        if(fdb.paramLire("PeriodeSam", "options").equals("1")){ cbSam.setSelected(true); }else{  cbLun.setSelected(false); }
        if(fdb.paramLire("PeriodeDim", "options").equals("1")){ cbDim.setSelected(true); }else{  cbLun.setSelected(false); }
    }

