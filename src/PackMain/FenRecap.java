    private void btnValidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidActionPerformed
        if(cbLun.isSelected()){
            fdb.paramEcrit("PeriodeLun", "1", "options");
        }else{
            fdb.paramEcrit("PeriodeLun", "0", "options");
        }
        if(cbMar.isSelected()){
            fdb.paramEcrit("PeriodeMar", "1", "options");
        }else{
            fdb.paramEcrit("PeriodeMar", "0", "options");
        }
        if(cbMerc.isSelected()){
            fdb.paramEcrit("PeriodeMerc", "1", "options");
        }else{
            fdb.paramEcrit("PeriodeMerc", "0", "options");
        }
        if(cbJeu.isSelected()){
            fdb.paramEcrit("PeriodeJeu", "1", "options");
        }else{
            fdb.paramEcrit("PeriodeJeu", "0", "options");
        }
        if(cbVen.isSelected()){
            fdb.paramEcrit("PeriodeVen", "1", "options");
        }else{
            fdb.paramEcrit("PeriodeVen", "0", "options");
        }
        if(cbSam.isSelected()){
            fdb.paramEcrit("PeriodeSam", "1", "options");
        }else{
            fdb.paramEcrit("PeriodeSam", "0", "options");
        }
        if(cbDim.isSelected()){
            fdb.paramEcrit("PeriodeDim", "1", "options");
        }else{
            fdb.paramEcrit("PeriodeDim", "0", "options");
        }
        fdb.paramEcrit("PeriodeHeure", tfHeure.getText(), "options");
        dispose();
    }//GEN-LAST:event_btnValidActionPerformed

