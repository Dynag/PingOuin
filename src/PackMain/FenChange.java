    public void lireXml(){
        String changeLog = "";
        try{
            
        URL url = new URL("https://prog.hemge.eu/PingOuin/changelog.xml");
        URLConnection con=url.openConnection();
        InputStream input = con.getInputStream();
        
        
        //File inputFile = new File("changelog.xml");
        DocumentBuilderFactory dbFactory = DocumentBuilderFactory.newInstance();
         DocumentBuilder dBuilder = dbFactory.newDocumentBuilder();
         Document doc = dBuilder.parse(input);
         doc.getDocumentElement().normalize();
         NodeList nList = doc.getElementsByTagName("Version");
         for (int temp = 0; temp < nList.getLength(); temp++) {
            Node nNode = nList.item(temp);
            
            if (nNode.getNodeType() == Node.ELEMENT_NODE) {
               Element eElement = (Element) nNode;
               changeLog = changeLog + "\n Version : " 
                  + eElement.getAttribute("number");
               changeLog = changeLog +"\n AmÃ©liorations : " 
                  + eElement
                  .getElementsByTagName("Ameliorations")
                  .item(0)
                  .getTextContent();
               changeLog = changeLog + "\n Corrections de bug : " 
                  + eElement
                  .getElementsByTagName("Bugs")
                  .item(0)
                  .getTextContent();
               changeLog = changeLog + "\n -------- \n";
               
            }
         }
      } catch (Exception e) {
         e.printStackTrace();
      }
        txtChange.setText(changeLog);
    }

